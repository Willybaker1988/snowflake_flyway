GRANT SELECT ON TABLE KAITO.AGENT TO ROLE SYSADMIN;
GRANT UPDATE ON TABLE KAITO.AGENT TO ROLE SYSADMIN;
GRANT REBUILD ON TABLE KAITO.AGENT TO ROLE SYSADMIN;
GRANT INSERT ON TABLE KAITO.AGENT TO ROLE SYSADMIN;
GRANT TRUNCATE ON TABLE KAITO.AGENT TO ROLE SYSADMIN;
